/*Second lesson from Udemity Java tutorial*/
/*"using variables"*/

public class tutorial_2 {
	
	public static void main(String[] args) {
		
		 int myNumber = 88;
		 short myShort = 847;
		 long myLong = 9797;
		 
		 double myDouble = 7.3243;
		 float myFloat = 324.3f; /*f at the end denotes float*/
		 
		 char myChar = 'y';
		 boolean myBoolean = false;
		 
		 byte myByte = 127;
		
		System.out.println("The value of myNumber is: " + myNumber);
		System.out.println("The value of myShort is: " + myShort);
		System.out.println("The value of myLong is: " + myLong);
		System.out.println("The value of myDouble is: " + myDouble);
		System.out.println("The value of myFloat is: " + myFloat);
		System.out.println("The value of myChar is: " + myChar);
		System.out.println("The value of MyBoolean: " + myBoolean);
		System.out.println("The value of MyByte: " + myByte);
	}
}
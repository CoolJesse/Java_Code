public class Song{

/** Fields ************************************************************************************/
	private String title;
	private double duration;
/**********************************************************************************************/
/** Constructor *******************************************************************************/
	public Song(String title, double duration){
		this.title = title;
		this.duration = duration;
	}
/**********************************************************************************************/
/** Getters ***********************************************************************************/
	public String getTitle(){
		return title;
	}
	public double getDuration(){
		return duration;
	}
/**********************************************************************************************/
}